function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  return ++n
}
function dec(n) {
  return --n
}

function makeInt(n) {
  n = parseInt('900' , 10)

  console.log(n)
}

function preserveDecimal(n) {
  n = parseFloat('2.222', 10)
  console.log(n)
}
